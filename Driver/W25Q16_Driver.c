void _W25Q16_Write(uint32 Addr,void *Data,uint16 Length)
{
	uint16 c,i;
	c=Length/SPI_FLASH_PageSize;
	if(Length%SPI_FLASH_PageSize)c++;
	for(i=0;i<c;i++)SPI_FLASH_SectorErase(Addr+i*SPI_FLASH_PageSize);
	SPI_FLASH_BufferWrite(Data, Addr, Length);
}

void W25Q16_Erase(uint32 Addr,uint32 Length)
{
	uint32 i,n;
	i=Length/SPI_FLASH_PageSize;
	if(Length%SPI_FLASH_PageSize)i++;
	for(n=0;n<i;n++)SPI_FLASH_SectorErase(Addr+SPI_FLASH_PageSize*n);
}
