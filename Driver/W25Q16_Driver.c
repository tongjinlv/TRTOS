{
	SPI_FLASH_BufferRead(Data, Addr, Length);
}
uint16 W25Q16_GetPageSize()
{
	uint8 Buf[1];
	uint32 i;
	Buf[0]=0x00;
	for(i=0;i<8000;i++)SPI_FLASH_BufferWrite(&Buf[0], i, 1);
	W25Q16_Erase(0,1);
	for(i=0;i<8000;i++)
	{
		SPI_FLASH_BufferRead(&Buf[0], i, 1);
		if(Buf[0]==0x00)return i;
	}
	return 0;
}

